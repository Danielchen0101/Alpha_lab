// 3. 自定义render1WeekTick完整代码
console.log('\n3. 自定义render1WeekTick完整代码:');
console.log('```jsx');
console.log('// 1 Week 专用：自定义X轴标签渲染器（两层交错显示）');
console.log('const render1WeekTick = (props: any) => {');
console.log('  const { x, y, payload, index } = props;');
console.log('  ');
console.log('  if (!payload || !payload.value) {');
console.log('    return null;');
console.log('  }');
console.log('  ');
console.log('  try {');
console.log('    const date = new Date(payload.value);');
console.log('    ');
console.log('    if (isNaN(date.getTime())) {');
console.log('      return null;');
console.log('    }');
console.log('    ');
console.log('    // 格式化：月/日 小时:分钟');
console.log('    const month = date.getUTCMonth() + 1;');
console.log('    const day = date.getUTCDate();');
console.log('    const hour = date.getUTCHours();');
console.log('    const minute = date.getUTCMinutes();');
console.log('    const text = `${month}/${day} ${String(hour).padStart(2, \'0\')}:${String(minute).padStart(2, \'0\')}`;');
console.log('    ');
console.log('    // 两层交错显示逻辑');
console.log('    // 偶数索引（0, 2, 4...）：上层，dy = -10');
console.log('    // 奇数索引（1, 3, 5...）：下层，dy = +10');
console.log('    const dy = index % 2 === 0 ? -10 : 10;');
console.log('    ');
console.log('    return (');
console.log('      <text');
console.log('        x={x}');
console.log('        y={y + dy}');
console.log('        dy={0}');
console.log('        textAnchor="middle"');
console.log('        fill="#333"');
console.log('        fontSize={8}');
console.log('        fontFamily="Arial, sans-serif"');
console.log('      >');
console.log('        {text}');
console.log('      </text>');
console.log('    );');
console.log('  } catch (error) {');
console.log('    console.error(\'[1 Week] 自定义tick渲染失败:\', error);');
console.log('    return null;');
console.log('  }');
console.log('};');
console.log('```');

